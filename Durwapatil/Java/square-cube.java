// Java program to display the square and cube of given number



            class square
            {
              public static void main(String args[])
              {
                int a, s, c;
                a = 5;
                s = a * a;
                System.out.print("Square : "+ s + "\n");
                c = a * a * a;
                System.out.print("Cube : " + c);
              }
            }
            
            
